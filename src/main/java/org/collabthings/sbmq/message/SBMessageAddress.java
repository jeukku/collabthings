package org.collabthings.sbmq.message;

public class SBMessageAddress {
	private String key;
	private int port;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
}
